void cycle(int delay)
{
    gpio_put(CLK_OUT, true);
    sleep_ms(delay);
    gpio_put(CLK_OUT, false);
    sleep_ms(delay);
}

int main()
{
    stdio_init_all();

    // Set up the GPIO pins
    gpio_init_mask(0xFFFFFFFF);
    gpio_set_dir_in_masked(0xFFFFFFFF);

    // Pico's built-in LED
    gpio_init(PICO_DEFAULT_LED_PIN);
    gpio_set_dir(PICO_DEFAULT_LED_PIN, GPIO_OUT);

    // CLK pin
    gpio_init(CLK_OUT);
    gpio_set_dir(CLK_OUT, GPIO_OUT);

    // RST pin
    gpio_init(RST_OUT);
    gpio_set_dir(RST_OUT, GPIO_OUT);

    // Turn onboard LED on
    gpio_put(PICO_DEFAULT_LED_PIN, true);

    // Reset the Z180
    gpio_put(RST_OUT, false);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    cycle(CLK_DELAY);
    gpio_put(RST_OUT, true);

    sleep_ms(CLK_DELAY);

    uint32_t addr_bus = 0;
    bool mreq = 0;
    bool m1 = 0;
    bool rd = 0;

    uint32_t cycle = 1;

    while (true)
    {
        gpio_put(CLK_OUT, true);
        sleep_ms(CLK_DELAY);

        addr_bus = (gpio_get_all() & 0x0000FF00) >> 8;
        mreq = gpio_get(MREQ_IN);
        m1 = gpio_get(M1_IN);
        rd = gpio_get(RD_IN);

        sleep_ms(2);

        if (!mreq && !rd)
        {
            gpio_set_dir_out_masked(0xFF);
            gpio_put_masked(0xFF, rom[addr_bus]);

            printf("READ: %02X\r\n", rom[addr_bus]);
        }
        else
        {
            gpio_set_dir_in_masked(0xFF);
        }

        printf("%u ADDR: %04X | MREQ: %d | RD: %d | M1: %d\r\n", cycle, addr_bus, mreq, rd, m1);

        gpio_put(CLK_OUT, false);
        sleep_ms(CLK_DELAY);

        cycle++;
    }

    return 0;
}
