#define WR_PIN 27
#define RD_PIN 28

#define ADDR_WIDTH 12

#define MEMORY_SIZE 4096

#define CLK_DELAY_US 5 // 100 kHz

void clock_sim()
{
    while (true)
    {
        gpio_put(EXTAL_PIN, true);
        sleep_us(CLK_DELAY_US);
        gpio_put(EXTAL_PIN, false);
        sleep_us(CLK_DELAY_US);
    }
}

